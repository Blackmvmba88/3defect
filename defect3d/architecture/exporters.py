def export_mesh(mesh: CompositePart, filepath: str, format: str = "obj",
               include_materials: bool = True):
    """
    Exporta malla a formato especificado.
    Exports mesh to specified format.
    
    Args:
        mesh: Malla a exportar / Mesh to export
        filepath: Ruta del archivo / File path
        format: Formato ("obj", "fbx", "glb") / Format ("obj", "fbx", "glb")
        include_materials: Si incluir materiales / Whether to include materials
        
    Returns:
        Ruta del archivo exportado / Path to exported file
    """
    exporters = {
        "obj": OBJExporter,
        "fbx": FBXExporter,
        "glb": GLBExporter,
    }
    
    if format not in exporters:
        raise ValueError(f"Formato no soportado / Unsupported format: {format}")
        
    exporter_class = exporters[format]
    exporter = exporter_class(mesh)
    
    if format == "obj":
        return exporter.export(filepath, include_materials)
    else:
        return exporter.export(filepath)
