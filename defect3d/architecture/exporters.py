"""
Mesh Exporters / Exportadores de Malla

Exporta mallas biomiméticas a múltiples formatos.
Exports biomimetic meshes to multiple formats.
"""

import json
from typing import Dict, List, Tuple
from ..core.composite import CompositePart


class MeshExporter:
    """
    Exportador base para mallas / Base mesh exporter
    """
    
    def __init__(self, mesh: CompositePart):
        """
        Inicializa el exportador / Initializes the exporter
        
        Args:
            mesh: Malla a exportar / Mesh to export
        """
        self.mesh = mesh
        
    def _extract_geometry(self) -> Tuple[List, List, List]:
        """
        Extrae geometría de la malla.
        Extracts geometry from the mesh.
        
        Returns:
            Tupla de (vertices, faces, normals)
            Tuple of (vertices, faces, normals)
        """
        vertices = []
        faces = []
        normals = []
        
        # Simplificación: extraer posiciones de las partes
        # Simplification: extract positions from parts
        def extract_parts(part):
            """Recursively extract geometry from part and its sub-parts"""
            nonlocal vertices, faces, normals
            
            if hasattr(part, 'position'):
                pos = part.position
                vertices.append(pos)
                
            # Recursivamente extraer sub-partes / Recursively extract sub-parts
            if hasattr(part, 'parts'):
                # parts es una lista / parts is a list
                if isinstance(part.parts, list):
                    for sub_part in part.parts:
                        extract_parts(sub_part)
                # o un diccionario / or a dictionary
                elif isinstance(part.parts, dict):
                    for sub_part in part.parts.values():
                        extract_parts(sub_part)
            
        extract_parts(self.mesh)
        
        return vertices, faces, normals


class OBJExporter(MeshExporter):
    """
    Exportador a formato Wavefront OBJ / Wavefront OBJ format exporter
    """
    
    def export(self, filepath: str, include_materials: bool = True):
        """
        Exporta a formato OBJ / Exports to OBJ format
        
        Args:
            filepath: Ruta del archivo / File path
            include_materials: Si incluir archivo MTL / Whether to include MTL file
        """
        vertices, faces, normals = self._extract_geometry()
        
        with open(filepath, 'w') as f:
            # Encabezado / Header
            f.write("# Biomimetic Architecture Mesh\n")
            f.write("# Generated by 3defect\n\n")
            
            if include_materials:
                mtl_file = filepath.replace('.obj', '.mtl')
                f.write(f"mtllib {mtl_file.split('/')[-1]}\n\n")
                
            # Escribir vértices / Write vertices
            f.write("# Vertices\n")
            for i, v in enumerate(vertices):
                if isinstance(v, tuple) and len(v) >= 3:
                    f.write(f"v {v[0]:.6f} {v[1]:.6f} {v[2]:.6f}\n")
                    
            # Escribir normales / Write normals
            if normals:
                f.write("\n# Normals\n")
                for n in normals:
                    f.write(f"vn {n[0]:.6f} {n[1]:.6f} {n[2]:.6f}\n")
                    
            # Escribir caras / Write faces
            if faces:
                f.write("\n# Faces\n")
                for face in faces:
                    f.write(f"f {' '.join(map(str, face))}\n")
                    
        if include_materials:
            self._export_mtl(mtl_file)
            
        return filepath
        
    def _export_mtl(self, filepath: str):
        """
        Exporta archivo de materiales MTL / Exports MTL material file
        """
        with open(filepath, 'w') as f:
            f.write("# Biomimetic Architecture Materials\n")
            f.write("# Generated by 3defect\n\n")
            
            # Material para piel / Skin material
            f.write("newmtl Skin\n")
            f.write("Ka 0.9 0.9 0.85\n")
            f.write("Kd 0.9 0.9 0.85\n")
            f.write("Ks 0.3 0.3 0.3\n")
            f.write("Ns 96\n\n")
            
            # Material para costillas / Ribs material
            f.write("newmtl Ribs\n")
            f.write("Ka 0.95 0.95 0.9\n")
            f.write("Kd 0.95 0.95 0.9\n")
            f.write("Ks 0.2 0.2 0.2\n")
            f.write("Ns 64\n\n")
            
            # Material para vegetación / Vegetation material
            f.write("newmtl Vegetation\n")
            f.write("Ka 0.3 0.8 0.4\n")
            f.write("Kd 0.3 0.8 0.4\n")
            f.write("Ks 0.1 0.1 0.1\n")
            f.write("Ns 32\n\n")
            
            # Material para vidrio / Glass material
            f.write("newmtl Glass\n")
            f.write("Ka 0.8 0.9 0.95\n")
            f.write("Kd 0.8 0.9 0.95\n")
            f.write("Ks 0.9 0.9 0.9\n")
            f.write("Ns 256\n")
            f.write("d 0.3\n")  # Transparencia / Transparency
            f.write("Tr 0.7\n")


